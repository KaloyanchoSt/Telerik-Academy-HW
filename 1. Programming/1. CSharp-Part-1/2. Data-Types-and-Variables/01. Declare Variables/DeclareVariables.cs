namespace DeclareVariables
{
    using System;

    class DeclareVariables
    {
        static void Main()
        {
            ushort a = 52130;
            sbyte b = -115;
            byte d = 97;
            short e = -10000;
            int c = 4825932;

            Console.WriteLine("{0}, {1}, {2}, {3}, {4},", a, b, c, d, e);
        }
    }
}
